package ch.xwr.seicentobookit.business.model;

public class SolAlvValues {
	private double baseAlv = 0;
	private double totalSalary = 0;
	private double totalSol = 0;
	private double solValue = 0;
	private double coordValue = 0;
	
		
	public double getBaseAlv() {
		return baseAlv;
	}
	public void setBaseAlv(double baseAlv) {
		this.baseAlv = baseAlv;
	}
	public double getTotalSalary() {
		return totalSalary;
	}
	public void setTotalSalary(double totalSalary) {
		this.totalSalary = totalSalary;
	}
	public double getTotalSol() {
		return totalSol;
	}
	public void setTotalSol(double totalSol) {
		this.totalSol = totalSol;
	}
	public double getSolValue() {
		return solValue;
	}
	public void setSolValue(double solValue) {
		this.solValue = solValue;
	}
	public double getCoordValue() {
		return coordValue;
	}
	public void setCoordValue(double coordValue) {
		this.coordValue = coordValue;
	}
	
}
